    private void isiComboProdi() {
        try {
            Connection conn = new database().getConnection();
            ResultSet res = conn.createStatement().executeQuery("SELECT nama_prodi FROM prodi");
            cbProdi.removeAllItems();
            while (res.next()) {
                cbProdi.addItem(res.getString("nama_prodi"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load prodi: " + e.getMessage());
        }
    }
    
