    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     try {
        Connection conn = new database().getConnection();
        
        // Cari ID Prodi (tetap sama kodenya)
        String namaProdi = cbProdi.getSelectedItem().toString();
        PreparedStatement pstProdi = conn.prepareStatement("SELECT id_prodi FROM prodi WHERE nama_prodi = ?");
        pstProdi.setString(1, namaProdi);
        ResultSet rs = pstProdi.executeQuery();
        rs.next();
        int idProdi = rs.getInt("id_prodi");

        String sql;
        // GANTI 'mata_kuliah' MENJADI 'matkul' dan sesuaikan nama kolomnya
        if (isEdit) {
            sql = "UPDATE matkul SET nama_matkul=?, sks=?, semester=?, id_prodi=? WHERE id_matkul=?";
        } else {
            sql = "INSERT INTO matkul (nama_matkul, sks, semester, id_prodi, id_matkul) VALUES (?, ?, ?, ?, ?)";
        }

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, txtNamaMK.getText());
        pst.setString(2, txtSks.getText());
        pst.setString(3, txtSemester.getText());
        pst.setInt(4, idProdi);
        pst.setString(5, txtIdMK.getText());

        pst.execute();
        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        this.dispose();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

